#include "lem_in.h"

int	count_num(int k)
{
	int	i;
	int	sign;

	sign = 0;
	if (k < 0)
		sign = 1;
	if (k / 10 == 0)
		return (1 + sign);
	i = 0;
	while (k != 0)
	{
		k = k / 10;
		i++;
	}
	return (i + sign);
}
