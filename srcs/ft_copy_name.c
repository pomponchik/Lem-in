#include "lem_in.h"

char	*ft_copy_name(char *line, int i)
{
	char	*name;

    i--;
	name = ft_strnew(i);
	i--;
	while (i > -1)
	{
		name[i] = line[i];
		i--;
	}
	return (name);
}
