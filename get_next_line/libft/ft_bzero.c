#include <string.h>

void	ft_bzero(void *s, size_t len)
{
	size_t				i;
	unsigned char		*b;

	i = 0;
	b = (unsigned char *)s;
	while (i != len)
	{
		b[i] = '\0';
		i++;
	}
}
