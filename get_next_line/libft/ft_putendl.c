#include <unistd.h>

void	ft_putendl(char const *c)
{
	int		i;

	if (c)
	{
		i = 0;
		while (c[i])
		{
			write(1, &c[i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
}
